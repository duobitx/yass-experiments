#!/usr/bin/env python3
"""
Generate base/01_layout.yaml plus tus/ and edfs/ overlays for the
big-scale experiment (50 OneWeb satellites + 7 ESTRACK ground stations).

Source TLE snapshot: tools/oneweb-tle-snapshot.txt (fetched from celestrak).

Every satellite runs the periodic-agent and produces a 5 MB file every
~5 min; CHECK_INTERVAL_SECONDS is varied per sat (270..329s) so the
phases spread out within a few iterations.

Every satellite also carries a recurring hardware fault stream
(non-Destroy: bandwidth caps, link drops, disk full / failure) with a
per-sat seed for reproducibility.

Usage:
    python3 gen.py
"""

import pathlib
import sys

# geo-calculator has a compile-time MAXSAT=200 limit on the total FsNode count
# (sats + GS combined), so N_SATS + len(GROUND_STATIONS) must stay <= 200.
N_SATS = 50
HW_REF_SAT = "nano-sat"
HW_REF_GS = "ground-station-hwdef"

FILE_SIZE = "5M"
INTERVAL_BASE = 270   # seconds; per-sat actual = INTERVAL_BASE + (idx*7 % 60)

GROUND_STATIONS = [
    ("estrack-new-norcia", -31.048, 116.192),
    ("estrack-kiruna",      67.857,  20.964),
    ("estrack-redu",        50.000,   5.167),
    ("estrack-cebreros",    40.453,  -4.368),
    ("estrack-santa-maria", 36.974, -25.094),
    ("estrack-kourou",       5.251, -52.805),
    ("estrack-malargue",   -35.776, -69.398),
]

# EDFS engine: 3 containers with bootstrap-peer config. Bootstrap peer is
# estrack-new-norcia (present in every Layout). Keys taken from the
# working spain-shot/edfs setup.
EDFS_ENGINE_IMG = "ghcr.io/duobitx/yass-edfs-engine"
EDFS_NODE_IMG   = "ghcr.io/duobitx/yass-edfs-engine-node"
EDFS_PROXY_IMG  = "ghcr.io/duobitx/yass-edfs-engine-proxy"
EDFS_CLUSTER_SECRET = "50896c846aed59faeec45d1779e6b9ca6fac89d135d988b52c2f366f1b7f373d"
EDFS_SWARM_KEY      = "e0e6161ec71e8e8c6d18c64dd8ee37f178fc50cadf58f20d08c481131a09bbae"
EDFS_BOOTSTRAP_PEER = "estrack-new-norcia"
EDFS_BOOTSTRAP_PEER_ID  = "12D3KooWLJFr7nesNtEQrQ9PqyjT8kvxbdUqfF4xwZZKEzJHnEhT"
EDFS_BOOTSTRAP_PEER_KEY = "CAESQNU9ILPST19ucrp2ZzY4BN1+LnoLK5XnH86F8m2Ce3R7m7n8DOzBtVlE/3GS9UCxChfcU+Y/lkjR/T5nBJEujGY="
EDFS_REPLICATION_FACTOR = "2"

GS_LIST = ",".join(gs for gs, _, _ in GROUND_STATIONS)

# Recurring fault recipe shared with UC2/UC5: mean interval 5m, jitter
# 50%, duration ~30s +/- 50%. Cycle one of these fault types per sat so
# the population sees a balanced mix.
FAULT_TYPES = [
    "NetworkBandwidthReduced",
    "NetworkFailure",
    "DiskFull",
    "DiskFailure",
]

HERE = pathlib.Path(__file__).resolve().parent
ROOT = HERE.parent
TLE_FILE = HERE / "oneweb-tle-snapshot.txt"
LAYOUT_FILE   = ROOT / "base" / "01_layout.yaml"
TUS_EXPDEF    = ROOT / "tus"  / "02_experiment_definition.yaml"
TUS_EXP       = ROOT / "tus"  / "03_experiment.yaml"
EDFS_EXPDEF   = ROOT / "edfs" / "02_experiment_definition.yaml"
EDFS_EXP      = ROOT / "edfs" / "03_experiment.yaml"
EDFS_KUSTOM   = ROOT / "edfs" / "kustomization.yaml"


def parse_tle(path: pathlib.Path):
    lines = [ln.rstrip() for ln in path.read_text().splitlines() if ln.strip()]
    if len(lines) % 3 != 0:
        sys.exit(f"TLE file {path}: line count {len(lines)} is not a multiple of 3")
    for i in range(0, len(lines), 3):
        name = lines[i].strip().lower()
        yield name, lines[i + 1], lines[i + 2]


def write_layout(sats):
    out = [
        "# Auto-generated by tools/gen.py - do not edit by hand.",
        "apiVersion: int.esa.yass/v1",
        "kind: Layout",
        "metadata:",
        "  name: big-scale-layout",
        "spec:",
    ]
    for name, l1, l2 in sats:
        out += [
            f"  - fsNode: {name}",
            f"    nodeType: satellite",
            f"    orbit:",
            f"      tle:",
            f"        - \"{l1}\"",
            f"        - \"{l2}\"",
            f"    hardwareSpecRef: {HW_REF_SAT}",
        ]
    for gs, lat, lng in GROUND_STATIONS:
        out += [
            f"  - fsNode: {gs}",
            f"    nodeType: groundStation",
            f"    earthPosition:",
            f"      lat: {lat}",
            f"      lng: {lng}",
            f"      heightOverSeaLevel: 0",
            f"    hardwareSpecRef: {HW_REF_GS}",
        ]
    LAYOUT_FILE.write_text("\n".join(out) + "\n")


def sat_behaviour(idx: int, name: str) -> list[str]:
    interval = INTERVAL_BASE + (idx * 7) % 60       # 270..329s
    fault = FAULT_TYPES[idx % len(FAULT_TYPES)]
    seed  = 1000 + idx
    lines = [
        f"    - fsNode: {name}",
        f"      agent:",
        f"        image: ghcr.io/duobitx/yass-agent-periodic",
        f"        envsMap:",
        f"          FILE_SIZE: \"{FILE_SIZE}\"",
        f"          CHECK_INTERVAL_SECONDS: \"{interval}\"",
        f"          MAX_PHOTOS: \"0\"",
        f"      hardwareEvents:",
        f"        - name: random-{fault.lower()}",
        f"          type: {fault}",
        f"          startOffset: 1m",
        f"          schedule:",
        f"            intervalMean: 5m",
        f"            intervalJitterPercent: 50",
        f"            durationMean: 30s",
        f"            durationJitterPercent: 50",
        f"            seed: {seed}",
    ]
    if fault == "NetworkBandwidthReduced":
        lines += [
            f"          params:",
            f"            networkBandwidth:",
            f"              reductionPercent: 75",
        ]
    return lines


def gs_behaviour(gs: str) -> list[str]:
    return [
        f"    - fsNode: {gs}",
        f"      agent:",
        f"        image: ghcr.io/duobitx/yass-agent-receive-only",
        f"      hardwareEvents: []",
    ]


def write_expdef(path: pathlib.Path, sats):
    out = [
        "# Auto-generated by tools/gen.py - do not edit by hand.",
        "apiVersion: int.esa.yass/v1",
        "kind: ExperimentDefinition",
        "metadata:",
        "  name: big-scale",
        "spec:",
        '  maxDuration: "999999h"',
        "  behaviours:",
    ]
    for idx, (name, _l1, _l2) in enumerate(sats):
        out += sat_behaviour(idx, name)
    for gs, _lat, _lng in GROUND_STATIONS:
        out += gs_behaviour(gs)
    path.write_text("\n".join(out) + "\n")


def write_tus_experiment():
    out = f"""# Auto-generated by tools/gen.py - do not edit by hand.
apiVersion: int.esa.yass/v1
kind: Experiment
metadata:
  name: big-scale-experiment
spec:
  experimentDefRef: big-scale
  layoutDefRef: big-scale-layout
  engineContainers:
    - name: engine-tus
      image: "ghcr.io/duobitx/yass-tus-fs-engine"
      env:
        - name: GROUND_STATIONS
          value: "{GS_LIST}"
  start: true
"""
    TUS_EXP.write_text(out)


