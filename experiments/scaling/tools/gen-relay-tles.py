#!/usr/bin/env python3
"""
Generate synthetic relay-satellite TLEs by shifting the producer's RAAN and
mean anomaly. Used by E1 (delivery time vs constellation size) in PLAN.md.

The producer is ONEWEB-0008 (NORAD 44059). Relay satellites share the
producer's inclination, eccentricity, arg. of perigee and mean motion;
only RAAN and mean anomaly are spread.

Usage:
    python3 gen-relay-tles.py            # writes ../relay-tles.yaml
    python3 gen-relay-tles.py --stdout   # prints to stdout

This script is idempotent. Run once; commit the output.
"""

import argparse
import os
import sys

PRODUCER_NAME = "oneweb-0008"
PRODUCER_L1 = "1 44059U 19010C   25347.49126494  .00000026  00000+0  35053-4 0  9990"
PRODUCER_L2 = "2 44059  87.9045 265.7535 0001501  76.4950 283.6348 13.16596955327295"
N_RELAYS = 7


def tle_checksum(line: str) -> int:
    s = 0
    for c in line[:-1]:
        if c.isdigit():
            s += int(c)
        elif c == "-":
            s += 1
    return s % 10


def with_checksum(line: str) -> str:
    return line[:-1] + str(tle_checksum(line[:-1] + "0"))


def gen_relay(i: int, base_raan: float, base_ma: float) -> tuple[str, str]:
    step = 360.0 / (N_RELAYS + 1)
    raan = (base_raan + step * i) % 360.0
    ma = (base_ma + step * i) % 360.0
    norad = 90000 + i
    intl = f"25001{chr(ord('A') + (i - 1)):1s}  "
    l1 = (
        f"1 {norad:05d}U {intl} 25347.49126494  .00000026  00000+0  "
        f"35053-4 0  9990"
    )
    l2 = (
        f"2 {norad:05d}  87.9045 {raan:8.4f} 0001501  76.4950 "
        f"{ma:8.4f} 13.16596955327290"
    )
    return with_checksum(l1), with_checksum(l2)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--stdout", action="store_true")
    args = ap.parse_args()

    # Sanity-check the producer TLE.
    for ln in (PRODUCER_L1, PRODUCER_L2):
        if tle_checksum(ln) != int(ln[-1]):
            print(f"WARN: producer TLE checksum mismatch on: {ln}", file=sys.stderr)

    # Parse base RAAN and mean anomaly from the producer's L2.
    base_raan = float(PRODUCER_L2[17:25])
    base_ma = float(PRODUCER_L2[43:51])

    out_lines = [
        "# Auto-generated by tools/gen-relay-tles.py — do not edit by hand.",
        f"# Producer: {PRODUCER_NAME} (NORAD 44059, ONEWEB-0008).",
        f"# Relays share inclination/altitude/period; only RAAN and mean",
        f"# anomaly are shifted by 360/{N_RELAYS + 1}° per relay.",
        "relays:",
    ]
    for i in range(1, N_RELAYS + 1):
        l1, l2 = gen_relay(i, base_raan, base_ma)
        out_lines.append(f"  - name: relay-{i:02d}")
        out_lines.append(f"    tle:")
        out_lines.append(f"      - \"{l1}\"")
        out_lines.append(f"      - \"{l2}\"")
    out = "\n".join(out_lines) + "\n"

    if args.stdout:
        sys.stdout.write(out)
        return
    target = os.path.join(os.path.dirname(__file__), "..", "relay-tles.yaml")
    target = os.path.normpath(target)
    with open(target, "w") as f:
        f.write(out)
    print(f"wrote {target}")


if __name__ == "__main__":
    main()
