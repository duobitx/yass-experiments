def write_edfs_kustomization():
    out = """apiVersion: kustomize.config.k8s.io/v1beta1
kind: Kustomization
resources:
  - ../base
  - 02_experiment_definition.yaml
  - 03_experiment.yaml
namespace: big-scale-experiment-edfs
"""
    EDFS_KUSTOM.write_text(out)


def main():
    all_sats = list(parse_tle(TLE_FILE))
    if len(all_sats) < N_SATS:
        sys.exit(f"only {len(all_sats)} satellites in TLE snapshot, need {N_SATS}")
    sats = all_sats[:N_SATS]

    EDFS_EXPDEF.parent.mkdir(parents=True, exist_ok=True)

    write_layout(sats)
    write_expdef(TUS_EXPDEF, sats)
    write_expdef(EDFS_EXPDEF, sats)
    write_tus_experiment()
    write_edfs_experiment()
    write_edfs_kustomization()

    print(f"wrote {LAYOUT_FILE} ({N_SATS} sats + {len(GROUND_STATIONS)} GS)")
    print(f"wrote {TUS_EXPDEF}")
    print(f"wrote {TUS_EXP}")
    print(f"wrote {EDFS_EXPDEF}")
    print(f"wrote {EDFS_EXP}")
    print(f"wrote {EDFS_KUSTOM}")


if __name__ == "__main__":
    main()
